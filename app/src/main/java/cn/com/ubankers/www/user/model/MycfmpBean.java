package cn.com.ubankers.www.user.model;

public class MycfmpBean {
	private String nickName;//昵称
	private String userId;//用户的id
	private String userFaceId;//头像id
	private String productRealNameStatus;//绑定财富师的状态
	public String getNickName() {
		return nickName;
	}
	public void setNickName(String nickName) {
		this.nickName = nickName;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserFaceId() {
		return userFaceId;
	}
	public void setUserFaceId(String userFaceId) {
		this.userFaceId = userFaceId;
	}
	public String getProductRealNameStatus() {
		return productRealNameStatus;
	}
	public void setProductRealNameStatus(String productRealNameStatus) {
		this.productRealNameStatus = productRealNameStatus;
	}
	
}
